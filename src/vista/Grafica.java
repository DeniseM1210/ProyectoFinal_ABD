    private void btnGraficaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGraficaActionPerformed
        DefaultPieDataset datos = new DefaultPieDataset();
        int x = tablaGrafica.getRowCount();
        
        for(int i = 0; i < x; i++){
            int bd = (int) tablaGrafica.getValueAt(i, 0);
            datos.setValue(tablaGrafica.getValueAt(i, 1)+ "", bd);
        }
        
        JFreeChart graficoPastel = ChartFactory.createPieChart("Pref Type", datos, true, true, false);
        ChartPanel panel = new ChartPanel(graficoPastel);
        panel.setPreferredSize(new Dimension(400,200));
        
        panelGraf.setLayout(new BorderLayout());
        panelGraf.add(panel, BorderLayout.NORTH);
        pack();
        repaint();
    }//GEN-LAST:event_btnGraficaActionPerformed

